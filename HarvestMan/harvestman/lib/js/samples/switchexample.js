inputOnkeyup = function(event) {
        if (fixDblKey()) { return; }
        switch (event.keyCode) {
                case 38 : /* up */
                    break;
                case 40 : /* down */
                    break;
                case 37 : /* left */
                    break;
                case 39 : /* right */
                    break;
                case  9 : /* tab */
                    break;
                case 13 : /* enter */
                    break;
        }
}